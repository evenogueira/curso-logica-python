primeiro_numero = 5

segundo_numero = 3

#adiçao
print(primeiro_numero + segundo_numero)

#subtraçao

print(primeiro_numero - segundo_numero)

#multipliçao

print(primeiro_numero * segundo_numero)

#divisao

print(primeiro_numero / segundo_numero)

#modulo % retorna o resto de uma divisao

print(primeiro_numero % segundo_numero)

#Potencia ** eleva o valor a uma potencia

print(primeiro_numero ** segundo_numero)


